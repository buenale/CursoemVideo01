n = input('Digite algo, por favor: ')
print ('O tipo primitivo desse valor é {}'.format(type(n)))
print ('Esse valor tem espaços? {}'.format(n.isspace()))
print ('Esse valor é um número? {}'.format(n.isnumeric()))
print ('Esse valor é alfabético? {}'.format(n.isalpha()))
print ('Esse valor é alfanumérico? {}'.format(n.isalnum()))
print ('Esse valor está em Maiusc? {}'.format(n.isupper()))
print ('Esse valor está em minusc? {}'.format(n.islower()))
print ('Esse valor está em capital.? {}'.format(n.istitle()))



